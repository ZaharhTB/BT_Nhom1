    private void loadcb(){
        try{
        String sql="select ma_sp,ten_sp from SanPham";
        ResultSet rs = DBManager.GetData(sql);
        
        cb_msp.removeAllItems();
        cb_tsp.removeAllItems();
        spMap.clear();
        
        while(rs.next()){
            
            String ma=rs.getString("ma_sp");
            String ten=rs.getString("ten_sp");
            
            cb_msp.addItem(ma);
            cb_tsp.addItem(ten);     
            spMap.put(ma, ten);
        }
        rs.close();
        }catch(SQLException ex){
            Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
        }finally {
            DataBase db = new DataBase();
            db.Disconnect(); 
        }
        
    }
    private void updateCB() {
    cb_msp.addActionListener(e -> {
        String Masp = (String) cb_msp.getSelectedItem();
        if (Masp != null) {
            cb_tsp.setSelectedItem(spMap.get(Masp));
        }
    });
    
    cb_tsp.addActionListener(e -> {
        String Tensp = (String) cb_tsp.getSelectedItem();
        if (Tensp != null) {
            for (Map.Entry<String, String> entry : spMap.entrySet()) {
                if (entry.getValue().equals(Tensp)) {
                    cb_msp.setSelectedItem(entry.getKey());
                    break;
                }
            }
        }
    });
}
    private long getDg(String masp){
        String sql ="select dg from hdn where ma_sp='"+masp+"'";
        ResultSet rs = DBManager.GetData(sql);
        long dg=0;
        try{
            while(rs.next()){
                dg=rs.getInt("dg");
            }
        }catch(SQLException e){
            Logger.getLogger(PhieuNhap.class.getName()).log(Level.SEVERE, null, e);
        }finally {
            DataBase db = new DataBase();
            db.Disconnect(); 
        }
        return dg;
        
    }
    private boolean kt_tt(String masp) {
        DataBase db = new DataBase();
        try {
            db.Connect();
            String sql = "SELECT COUNT(*) FROM hdn WHERE ma_sp='" + masp + "'";
            ResultSet rs = db.GetData(sql);
            if (rs.next()) {
                int count = rs.getInt(1);
                return count > 0;
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            db.Disconnect(); // Đóng kết nối trong khối finally
        }
        return false;
    }

