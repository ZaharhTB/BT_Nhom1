    private void btn_giamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_giamActionPerformed
        // TODO add your handling code here:
        if(btn_giam.getText().equalsIgnoreCase("Giảm Giá")){
            btn_giam.setText("Hủy");
            btn_tang.setText("Lưu");
            btn_thoat.setEnabled(false);
        }
        else if(btn_giam.getText().equalsIgnoreCase("Lưu")){
            int pt = 0;
            try{
                pt=Integer.parseInt(txt_pt.getText());
            }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập số","Thong bao",JOptionPane.ERROR_MESSAGE);
                    txt_pt.requestFocus();
                    txt_pt.selectAll();
                    return;
            }if(pt<1 || pt >100){
                JOptionPane.showMessageDialog(null, "Tỷ lệ % tối thiểu là 1% hoặc tối đa là 100%","Thong bao",JOptionPane.ERROR_MESSAGE);
                txt_pt.requestFocus();
                txt_pt.selectAll();
                return ;
            }else{               
                if (kt_tt(cb_msp.getSelectedItem().toString())) {                
                    long dg_cu = getDg(cb_msp.getSelectedItem().toString());
                    float dg_moi = (float) Math.floor(dg_cu + (dg_cu * (pt / 100.0f)) + 0.5f);
                    String sql = "update hdn set dg='" + dg_moi + "' where ma_sp='" + cb_msp.getSelectedItem().toString() + "'";
                    DBManager.Update(sql);
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thong bao", JOptionPane.INFORMATION_MESSAGE);
                    JOptionPane.showMessageDialog(null, " Giá Cũ = " + dg_cu + "\nGiá Mới = " + dg_moi, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Hiện tại sản phẩm này chưa có hàng", "Thông báo", JOptionPane.ERROR_MESSAGE);
                } 
            }
        }
        else{
            btn_tang.setText("Tăng Giá");
            btn_giam.setText("Giảm Giá");
            btn_thoat.setEnabled(true);            
        }        
    }//GEN-LAST:event_btn_giamActionPerformed

