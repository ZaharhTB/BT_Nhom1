    private void ClickAddText(){
            j.getSelectionModel().addListSelectionListener(event->
            {
                if(!event.getValueIsAdjusting() && j.getSelectedRow() != -1){
                    int row = j.getSelectedRow();
                    txt_mncc.setText(j.getValueAt(row, 0).toString());
                    txt_tncc.setText(j.getValueAt(row, 1).toString());
                    txt_dc.setText(j.getValueAt(row, 2).toString());
                    txt_sdt.setText(j.getValueAt(row, 3).toString());
                    txt_vt.setText(String.valueOf(row+1));
                }
            });
    }
    private void load(){
        sql="select * from NhaCungCap";
        DBManager.Table(j, sql);     
    }
    private void addText(int rd){
        if(rd >= 0 && rd < j.getRowCount()){
            txt_mncc.setText(j.getValueAt(rd, 0).toString());
            txt_tncc.setText(j.getValueAt(rd, 1).toString());
            txt_dc.setText(j.getValueAt(rd, 2).toString());
            txt_sdt.setText(j.getValueAt(rd, 3).toString());
        }
    }
    private void clear(){
        Random rd = new Random();
        String mncc="CT"+rd.nextInt(500);
        txt_mncc.setText(mncc);
        txt_tncc.setText("");
        txt_dc.setText("");
        txt_sdt.setText("");
    }
